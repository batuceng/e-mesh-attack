class Point_Transformer_Last(nn.Module):
    def __init__(self, args, channels=256):
        super(Point_Transformer_Last, self).__init__()
        self.args = args
        self.conv1 = nn.Conv1d(channels, channels, kernel_size=1, bias=False)
        self.conv2 = nn.Conv1d(channels, channels, kernel_size=1, bias=False)

        self.bn1 = nn.InstanceNorm1d(channels)
        self.bn2 = nn.InstanceNorm1d(channels)


        self.sa1 = SA_Layer(channels)
        self.sa2 = SA_Layer(channels)
        self.sa3 = SA_Layer(channels)
        self.sa4 = SA_Layer(channels)

    def forward(self, x):
        # 
        # b, 3, npoint, nsample  
        # conv2d 3 -> 128 channels 1, 1
        # b * npoint, c, nsample 
        # permute reshape
        batch_size, _, N = x.size()

        # B, D, N
        x = F.relu(self.bn1(self.conv1(x)))
        x = F.relu(self.bn2(self.conv2(x)))

        x1 = self.sa1(x)#torch.Size([16, 256, 256])
        x2 = self.sa2(x1)#torch.Size([16, 256, 256])
        x3 = self.sa3(x2)#torch.Size([16, 256, 256])
        x4 = self.sa4(x3)#torch.Size([16, 256, 256])

        x = torch.cat((x1, x2, x3, x4), dim=1)

        return x

    def forward_denoised(self, x, denoiser):
        # 
        # b, 3, npoint, nsample  
        # conv2d 3 -> 128 channels 1, 1
        # b * npoint, c, nsample 
        # permute reshape
        batch_size, _, N = x.size()

        layers = []

        # B, D, N
        x = F.relu(self.bn1(self.conv1(x)))
        x = F.relu(self.bn2(self.conv2(x)))


        #Layer 3
        x1 = self.sa1(x)#torch.Size([16, 256, 256])
        x2 = self.sa2(x1)#torch.Size([16, 256, 256])
        x2 = denoiser(x2, layer=3)
        layers.append(x2.clone().detach().requires_grad_(True))

        #Layer 4
        x3 = self.sa3(x2)#torch.Size([16, 256, 256])
        x4 = self.sa4(x3)#torch.Size([16, 256, 256])
        x4 = denoiser(x4, layer=4)
        layers.append(x4.clone().detach().requires_grad_(True))



        x = torch.cat((x1, x2, x3, x4), dim=1)

        return x, layers

class SA_Layer(nn.Module):
    def __init__(self, channels):
        super(SA_Layer, self).__init__()
        self.q_conv = nn.Conv1d(channels, channels // 4, 1, bias=False)
        self.k_conv = nn.Conv1d(channels, channels // 4, 1, bias=False)
        self.q_conv.weight = self.k_conv.weight
        self.q_conv.bias = self.k_conv.bias

        self.v_conv = nn.Conv1d(channels, channels, 1)
        self.trans_conv = nn.Conv1d(channels, channels, 1)
        self.after_norm = nn.InstanceNorm1d(channels)
        self.act = nn.ReLU()
        self.softmax = nn.Softmax(dim=-1)

    def forward(self, x):
        # b, n, c
        x_q = self.q_conv(x).permute(0, 2, 1)
        # b, c, n
        x_k = self.k_conv(x)
        x_v = self.v_conv(x)
        # b, n, n
        energy = torch.bmm(x_q, x_k)

        attention = self.softmax(energy)
        attention = attention / (1e-9 + attention.sum(dim=1, keepdim=True))
        # b, c, n
        x_r = torch.bmm(x_v, attention)
        x_r = self.act(self.after_norm(self.trans_conv(x - x_r)))
        x = x + x_r
        return x
    
    
    
